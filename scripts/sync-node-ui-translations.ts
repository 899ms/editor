Object.assign(exactZh, FULL_NODE_UI_ZH)
Object.assign(termsZh, FULL_NODE_TERMS_ZH)

for (const locale of ['en', 'zh-CN'] as const) {
  const file = resolve(root, `packages/i18n/src/locales/${locale}/nodes.json`)
  const resource = JSON.parse(await readFile(file, 'utf8')) as Record<string, unknown>
  const exact = locale === 'zh-CN' ? exactZh : Object.fromEntries(Object.keys(exactZh).map((key) => [key, key]))
  const terms = locale === 'zh-CN'
    ? termsZh
    : Object.fromEntries(Object.keys(termsZh).map((key) => [key, key]))
  resource.uiText = exact
  resource.uiTerms = terms
  await writeFile(file, `${JSON.stringify(resource, null, 2)}\n`, 'utf8')
}

console.log(`Synchronized ${Object.keys(exactZh).length} exact labels and ${Object.keys(termsZh).length} UI terms.`)
